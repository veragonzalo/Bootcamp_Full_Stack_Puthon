# Imagina que esto es el saldo de una cuenta
saldo = 1000  # Asignación simple: guardamos 1000
print("Saldo inicial:", saldo)

saldo += 200  # Sumamos 200 y guardamos el nuevo saldo
print("Saldo después de depositar 200:", saldo)

saldo -= 150  # Restamos 150 y guardamos
print("Saldo después de pagar 150:", saldo)

saldo *= 2  # Duplicamos el saldo (ejemplo didáctico)
print("Saldo duplicado:", saldo)

saldo /= 4  # Dividimos el saldo en 4 (ojo: puede quedar como float)
print("Saldo dividido en 4:", saldo)

saldo %= 300  # Guardamos el resto de dividir el saldo por 300
print("Saldo mod 300 (resto):", saldo)