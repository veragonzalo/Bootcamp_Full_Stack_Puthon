edad = 17  # Guardamos una edad

print("¿Edad es 18?:", edad == 18)     # False, porque 17 no es 18
print("¿Edad es distinta de 18?:", edad != 18)  # True
print("¿Edad es mayor que 15?:", edad > 15)     # True
print("¿Edad es menor que 18?:", edad < 18)     # True
print("¿Edad es mayor o igual que 18?:", edad >= 18)  # False
print("¿Edad es menor o igual que 17?:", edad <= 17)  # True