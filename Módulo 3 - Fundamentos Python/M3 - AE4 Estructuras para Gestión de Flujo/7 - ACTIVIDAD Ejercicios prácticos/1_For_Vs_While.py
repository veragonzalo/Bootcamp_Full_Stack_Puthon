# --------------------------------------------
# for: cuando sabes qué vas a recorrer (por cada...)
# --------------------------------------------

alumnos = ["Ana", "Diego", "Felipe"]  # Lista de alumnos

for alumno in alumnos:  # Por cada alumno en la lista...
    print("Alumno:", alumno)  # Imprimimos el nombre del alumno


# --------------------------------------------
# while: cuando repites mientras se cumpla una condición
# --------------------------------------------

contador = 1  # Variable que cambia
while contador <= 3:  # Mientras contador sea <= 3...
    print("Vuelta número:", contador)  # Mostramos la vuelta
    contador += 1  # Incrementamos para que el ciclo avance y termine